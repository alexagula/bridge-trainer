/**
 * Get scenarios filtered by category.
 * @param {string} category — 'all' | 'signal' | 'discard' | 'count'
 * @returns {Array}
 */
export function getDefenseScenariosByCategory(category) {
  if (!category || category === 'all') return DEFENSE_SCENARIOS;
  return DEFENSE_SCENARIOS.filter(s => s.category === category);
}

